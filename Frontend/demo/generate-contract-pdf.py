#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Генератор демо-PDF договора для ContractPro (dev:e2e без backend).

Текст договора намеренно совпадает с фикстурой анализа
(Frontend/tests/msw/fixtures/results.ts → analysisResults), чтобы на экране
«Результат проверки договора» данные соответствовали загруженному документу:

  - тип: услуги; стороны: ООО «Контракт-Сервис» (Исполнитель) и
    ООО «Альфа» (Заказчик); предмет: оказание консалтинговых услуг;
    цена: 1 500 000 руб.; срок: 12 месяцев; юрисдикция: г. Москва;
  - п. 4.2 содержит «Исполнитель несёт ответственность за нарушение сроков.»
    без предела ответственности         → risk-1 (high, ст. 330 ГК РФ);
  - п. 7.1 «Заказчик вправе в любой момент отказаться от договора.» без
    компенсации расходов                 → risk-2 (high, ст. 782 ГК РФ);
  - раздел о порядке урегулирования споров отсутствует
                                         → risk-3 (medium, ст. 4 АПК РФ);
  - в теле договора нет ссылок на приложения
                                         → risk-4 (low).

Запуск:  python3 demo/generate-contract-pdf.py
Зависимости:  pip install fpdf2   (использует системный Times New Roman).
"""

from fpdf import FPDF

FONT_DIR = "/System/Library/Fonts/Supplemental"
OUT = "Договор оказания услуг (демо).pdf"

pdf = FPDF(format="A4")
pdf.set_margins(left=25, top=20, right=15)
pdf.set_auto_page_break(auto=True, margin=20)
pdf.add_font("TNR", "", f"{FONT_DIR}/Times New Roman.ttf")
pdf.add_font("TNR", "B", f"{FONT_DIR}/Times New Roman Bold.ttf")
pdf.add_font("TNR", "I", f"{FONT_DIR}/Times New Roman Italic.ttf")
pdf.add_page()

LH = 6  # line height


def title(text):
    pdf.set_font("TNR", "B", 14)
    pdf.multi_cell(0, 7, text, align="C", new_x="LMARGIN", new_y="NEXT")
    pdf.ln(1)


def heading(text):
    pdf.ln(2)
    pdf.set_font("TNR", "B", 12)
    pdf.multi_cell(0, LH, text, align="L", new_x="LMARGIN", new_y="NEXT")
    pdf.ln(0.5)


def para(text, align="J"):
    pdf.set_font("TNR", "", 12)
    pdf.multi_cell(0, LH, text, align=align, new_x="LMARGIN", new_y="NEXT")


title("ДОГОВОР № КС-2026/04-17\nоб оказании консалтинговых услуг")
pdf.ln(1)

pdf.set_font("TNR", "", 12)
pdf.cell(0, LH, "г. Москва", align="L", new_x="LMARGIN", new_y="NEXT")
pdf.cell(0, LH, "«15» апреля 2026 г.", align="L", new_x="LMARGIN", new_y="NEXT")
pdf.ln(1)

para(
    "Общество с ограниченной ответственностью «Контракт-Сервис», именуемое в "
    "дальнейшем «Исполнитель», в лице генерального директора Смирнова Андрея "
    "Викторовича, действующего на основании Устава, с одной стороны, и"
)
para(
    "Общество с ограниченной ответственностью «Альфа», именуемое в дальнейшем "
    "«Заказчик», в лице генерального директора Петровой Марии Сергеевны, "
    "действующего на основании Устава, с другой стороны, совместно именуемые "
    "«Стороны», заключили настоящий Договор о нижеследующем."
)

heading("1. Предмет договора")
para(
    "1.1. Исполнитель обязуется по заданию Заказчика оказать консалтинговые "
    "услуги в области управленческого и финансового консультирования, а "
    "Заказчик обязуется оплатить эти услуги в порядке и на условиях, "
    "предусмотренных настоящим Договором."
)
para(
    "1.2. Конкретный перечень и объём услуг определяются Сторонами в рамках "
    "согласованных заданий на оказание услуг."
)

heading("2. Права и обязанности сторон")
para("2.1. Исполнитель обязуется оказать услуги качественно и в согласованные сроки.")
para(
    "2.2. Заказчик обязуется предоставить Исполнителю необходимую информацию и "
    "документы, а также своевременно оплатить оказанные услуги."
)

heading("3. Стоимость услуг и порядок расчётов")
para(
    "3.1. Общая стоимость услуг по настоящему Договору составляет "
    "1 500 000 (один миллион пятьсот тысяч) рублей 00 копеек, НДС не облагается."
)
para(
    "3.2. Оплата производится Заказчиком в безналичном порядке путём "
    "перечисления денежных средств на расчётный счёт Исполнителя."
)

heading("4. Ответственность сторон")
para(
    "4.1. За нарушение срока оплаты Заказчик уплачивает Исполнителю неустойку в "
    "размере 0,1% от неоплаченной суммы за каждый день просрочки."
)
para("4.2. Исполнитель несёт ответственность за нарушение сроков.")
para(
    "4.3. Во всех остальных случаях Стороны несут ответственность в "
    "соответствии с действующим законодательством Российской Федерации."
)

heading("5. Порядок сдачи-приёмки услуг")
para(
    "5.1. По факту оказания услуг Исполнитель представляет Заказчику акт "
    "об оказании услуг."
)
para(
    "5.2. Заказчик обязан рассмотреть и подписать акт либо представить "
    "мотивированный отказ в течение 5 (пяти) рабочих дней."
)

heading("6. Срок действия договора")
para(
    "6.1. Настоящий Договор вступает в силу с момента его подписания обеими "
    "Сторонами и действует в течение 12 (двенадцати) месяцев."
)

heading("7. Изменение и расторжение договора")
para("7.1. Заказчик вправе в любой момент отказаться от договора.")
para(
    "7.2. Все изменения и дополнения к настоящему Договору действительны при "
    "условии, что они совершены в письменной форме и подписаны Сторонами."
)

heading("8. Прочие условия")
para(
    "8.1. Настоящий Договор составлен в двух экземплярах, имеющих одинаковую "
    "юридическую силу, по одному для каждой из Сторон."
)
para(
    "8.2. Во всём, что не предусмотрено настоящим Договором, Стороны "
    "руководствуются действующим законодательством Российской Федерации."
)

heading("9. Реквизиты и подписи сторон")
pdf.ln(1)
pdf.set_font("TNR", "B", 12)
col_w = (pdf.epw) / 2
y0 = pdf.get_y()
pdf.multi_cell(col_w, LH, "ИСПОЛНИТЕЛЬ", align="L", new_x="RIGHT", new_y="TOP")
pdf.multi_cell(col_w, LH, "ЗАКАЗЧИК", align="L", new_x="LMARGIN", new_y="NEXT")
pdf.set_font("TNR", "", 12)
left = (
    "ООО «Контракт-Сервис»\n"
    "ИНН 7701234567 / КПП 770101001\n"
    "г. Москва, ул. Тверская, д. 10\n\n"
    "Генеральный директор\n\n"
    "______________ / А.В. Смирнов /"
)
right = (
    "ООО «Альфа»\n"
    "ИНН 7707654321 / КПП 770701001\n"
    "г. Москва, Ленинский пр-т, д. 25\n\n"
    "Генеральный директор\n\n"
    "______________ / М.С. Петрова /"
)
y1 = pdf.get_y()
pdf.set_xy(pdf.l_margin, y1)
pdf.multi_cell(col_w, LH, left, align="L", new_x="RIGHT", new_y="TOP")
pdf.set_xy(pdf.l_margin + col_w, y1)
pdf.multi_cell(col_w, LH, right, align="L")

pdf.output(OUT)
print(f"OK: {OUT}")
